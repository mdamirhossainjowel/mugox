#!/usr/bin/env node
/**
 * AUTO-DISCOVERS every tool under /tools and generates:
 *   - lib/tools/registry.generated.ts      (all ToolMeta entries, for SEO/sitemap/registry)
 *   - lib/tools/componentMap.generated.tsx (code-split dynamic() map, only for tools with a UI)
 *
 * This lets engineers add a tool by dropping a folder in /tools — no manual
 * registration, no import list to maintain by hand. Designed to scale to 5000+ tools.
 *
 * Runs automatically via "predev" / "prebuild" (see package.json).
 * Do not hand-edit the *.generated.* output files — they are overwritten every run.
 */
import { readdirSync, statSync, existsSync, writeFileSync, readFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const TOOLS_DIR = join(ROOT, "tools");
const CATEGORIES_FILE = join(ROOT, "config", "categories.ts");
const REGISTRY_OUT = join(ROOT, "lib", "tools", "registry.generated.ts");
const COMPONENT_MAP_OUT = join(ROOT, "lib", "tools", "componentMap.generated.tsx");

function isDir(path) {
  return existsSync(path) && statSync(path).isDirectory();
}

function getValidCategorySlugs() {
  const src = readFileSync(CATEGORIES_FILE, "utf8");
  const matches = [...src.matchAll(/slug:\s*"([a-z0-9-]+)"/g)];
  return new Set(matches.map((m) => m[1]));
}

function toPascalCase(str) {
  return str
    .split(/[-_]/g)
    .filter(Boolean)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join("");
}

function toCamelCase(str) {
  const pascal = toPascalCase(str);
  return pascal.charAt(0).toLowerCase() + pascal.slice(1);
}

function discoverTools() {
  const validCategories = getValidCategorySlugs();
  const entries = [];
  const slugToCategory = new Map();

  if (!isDir(TOOLS_DIR)) return entries;

  const categoryDirs = readdirSync(TOOLS_DIR).filter((d) => isDir(join(TOOLS_DIR, d)));

  for (const category of categoryDirs) {
    if (!validCategories.has(category)) {
      throw new Error(
        `[generate-registry] tools/${category}/ has no matching entry in config/categories.ts. ` +
          `Add the category there first, or fix the folder name.`
      );
    }

    const categoryPath = join(TOOLS_DIR, category);
    const toolDirs = readdirSync(categoryPath).filter((d) => isDir(join(categoryPath, d)));

    for (const slug of toolDirs) {
      const toolPath = join(categoryPath, slug);
      const metaPath = join(toolPath, "meta.ts");
      const indexPath = join(toolPath, "index.tsx");

      if (!existsSync(metaPath)) {
        console.warn(`[generate-registry] Skipping tools/${category}/${slug} — missing meta.ts`);
        continue;
      }

      if (slugToCategory.has(slug)) {
        throw new Error(
          `[generate-registry] Duplicate tool slug "${slug}" found in both ` +
            `"${slugToCategory.get(slug)}" and "${category}". Tool slugs must be globally unique ` +
            `(routes are /[category]/[tool], and the registry is keyed by slug).`
        );
      }
      slugToCategory.set(slug, category);

      entries.push({
        category,
        slug,
        hasComponent: existsSync(indexPath),
        importName: toCamelCase(slug),
      });
    }
  }

  return entries.sort((a, b) => (a.category + "/" + a.slug).localeCompare(b.category + "/" + b.slug));
}

/** Guards against camelCase import-name collisions (rare, but cheap to check at 5000+ scale). */
function assignUniqueImportNames(entries) {
  const used = new Map();
  for (const e of entries) {
    let name = e.importName;
    if (used.has(name)) {
      name = toCamelCase(`${e.category}-${e.slug}`);
      let i = 2;
      while (used.has(name)) {
        name = `${toCamelCase(`${e.category}-${e.slug}`)}${i++}`;
      }
    }
    used.set(name, true);
    e.importName = name;
  }
}

function writeRegistry(entries) {
  const header = `// AUTO-GENERATED FILE — DO NOT EDIT DIRECTLY
// Regenerated by scripts/generate-registry.mjs on every "npm run dev" / "npm run build".
// Source of truth: tools/<category>/<slug>/meta.ts
// eslint-disable

import type { ToolMeta } from "@/types";
`;

  const imports = entries
    .map((e) => `import ${e.importName} from "@/tools/${e.category}/${e.slug}/meta";`)
    .join("\n");

  const arrayBody = entries.map((e) => `  ${e.importName},`).join("\n");

  const content = `${header}
${imports}

export const TOOL_REGISTRY: ToolMeta[] = [
${arrayBody}
];
`;

  writeFileSync(REGISTRY_OUT, content);
}

function writeComponentMap(entries) {
  const withComponent = entries.filter((e) => e.hasComponent);

  const header = `// AUTO-GENERATED FILE — DO NOT EDIT DIRECTLY
// Regenerated by scripts/generate-registry.mjs on every "npm run dev" / "npm run build".
// Lazily code-splits each tool's UI so heavy libraries only ship to the pages that need them.
// A tool with a meta.ts but no index.tsx yet is intentionally omitted here — its SEO page
// still renders (via TOOL_REGISTRY) and falls back to a "coming soon" state.
// eslint-disable
"use client";

import dynamic from "next/dynamic";
import type { ComponentType } from "react";
import { ToolLoadingState } from "@/components/tools/ToolLoadingState";
`;

  const mapBody = withComponent
    .map(
      (e) =>
        `  "${e.slug}": dynamic(() => import("@/tools/${e.category}/${e.slug}"), { ssr: false, loading: () => <ToolLoadingState /> }),`
    )
    .join("\n");

  const content = `${header}
export const TOOL_COMPONENTS: Record<string, ComponentType> = {
${mapBody}
};
`;

  writeFileSync(COMPONENT_MAP_OUT, content);
}

const entries = discoverTools();
assignUniqueImportNames(entries);
writeRegistry(entries);
writeComponentMap(entries);

console.log(
  `[generate-registry] ${entries.length} tool(s) registered, ` +
    `${entries.filter((e) => e.hasComponent).length} with live components.`
);
